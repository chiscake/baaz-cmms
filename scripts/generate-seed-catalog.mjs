function locId(building, workshop = 0, section = 0) {
  const b = String(building).padStart(3, "0");
  const w = String(workshop).padStart(3, "0");
  const s = String(section).padStart(3, "0");
  return `a1000000-0000-4000-8000-${b}${w}${s}000`;
}

