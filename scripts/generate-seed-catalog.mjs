/** @type {{ id: string, parent: string|null, name: string, code: string }[]} */
const subs = [];

function addSub(building, workshop, section, name, code, parentBuilding, parentWorkshop, parentSection = 0) {
  const parent =
    parentWorkshop === null
      ? locId(parentBuilding ?? building, 0, 0)
      : locId(parentBuilding ?? building, parentWorkshop, parentSection);
  subs.push({ id: locId(building, workshop, section), parent, name, code });
}

function addWorkshopWithSections(building, wNum, wName, wCode, sections) {
  addSub(building, wNum, 0, wName, wCode, building, null);
  sections.forEach(([sNum, sName, sCode], i) => {
    addSub(building, wNum, sNum || i + 1, sName, sCode, building, wNum);
  });
}

function addLeafWorkshop(building, wNum, wName, wCode) {
  addSub(building, wNum, 0, wName, wCode, building, null);
}

function addMinimalZones(building, zones) {
  zones.forEach(([wNum, name, code], i) => {
    addSub(building, wNum || i + 1, 0, name, code, building, null);
  });
}

